/** Get hospitals filtered by taluka */
export function getHospitalsByTaluka(taluka: string): PalgharHospital[] {
  return PALGHAR_HOSPITALS.filter((h) => h.taluka === taluka);
}

/** Get a hospital by its ID */
export function getHospitalById(id: number): PalgharHospital | undefined {
  return PALGHAR_HOSPITALS.find((h) => h.id === id);
}

/** Get hospitals suitable for 24x7 emergency services */
export function getEmergencyHospitals(): PalgharHospital[] {
  return PALGHAR_HOSPITALS.filter((h) => h.services_24x7);
}

/** Get grouped hospitals by taluka */
export function getHospitalsGroupedByTaluka(): Record<string, PalgharHospital[]> {
  return PALGHAR_TALUKAS.reduce((acc, taluka) => {
    acc[taluka] = getHospitalsByTaluka(taluka);
    return acc;
  }, {} as Record<string, PalgharHospital[]>);
}

